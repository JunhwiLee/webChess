# ── Color palettes ─────────────────────────────────────────────────────────
PALETTES = {
    'white': {
        '1': '#F0EDE0',   # body
        '2': '#B0A870',   # shadow / outline
    },
    'black': {
        '1': '#403830',   # body
        '2': '#201810',   # shadow / outline
    },
}

CELL = 4   # each pixel → 4×4 SVG units  →  64×64 final

def make_svg(pixels, palette):
    size = CELL * 16
    rects = []
    for r, row in enumerate(pixels):
        for c, ch in enumerate(row):
            if ch == '0':
                continue
            color = palette.get(ch, '#ff00ff')
            x, y = c * CELL, r * CELL
            rects.append(
                f'<rect x="{x}" y="{y}" width="{CELL}" height="{CELL}" fill="{color}"/>'
            )
    body = '\n  '.join(rects)
    return (
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'width="{size}" height="{size}" '
        f'viewBox="0 0 {size} {size}" '
        f'shape-rendering="crispEdges">\n  '
        f'{body}\n</svg>\n'
    )

for color, palette in PALETTES.items():
    for piece_type, pixels in PIXELS.items():
        svg = make_svg(pixels, palette)
        path = os.path.join(OUT_DIR, f'{color}_{piece_type}.svg')
        with open(path, 'w') as f:
            f.write(svg)
        print(f'  written: {path}')

print('Done.')
